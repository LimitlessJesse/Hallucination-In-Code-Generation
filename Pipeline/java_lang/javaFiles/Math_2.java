import java.lang.Math;

public class Math_2 {
    public static void main(String[] args) {
        double a = 10.4;
        double b = 10.5;
        double c = 10.6;

        System.out.println("Math.ceil(10.4) = " + Math.ceil(a));
        System.out.println("Math.ceil(10.5) = " + Math.ceil(b));
        System.out.println("Math.ceil(10.6) = " + Math.ceil(c));
    }
}
