import java.lang.Math;

public class Math_3 {
    public static void main(String[] args) {
        double a = 9.2;
        double b = 9.8;
        double c = 10.0;

        System.out.println("Math.floor(" + a + ") = " + Math.floor(a));
        System.out.println("Math.floor(" + b + ") = " + Math.floor(b));
        System.out.println("Math.floor(" + c + ") = " + Math.floor(c));
    }
}
