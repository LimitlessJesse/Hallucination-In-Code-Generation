import java.lang.Math;

public class Math_18 {
    public static void main(String[] args) {
        double f1 = 10.0;
        double f2 = 3.0;
        double remainder = Math.IEEEremainder(f1, f2);
        System.out.println("The remainder of " + f1 + " divided by " + f2 + " is " + remainder);
    }
}
